def njl_gap_equation(M, m_current, G_val, Lambda_cut, N_colors=3):
    """NJL gap equation with 3D sharp cutoff (standard convention).

    M = m + 2G * N_c / pi^2 * M * I_1(M, Lambda)

    where I_1 = int_0^Lambda dp p^2/sqrt(p^2 + M^2)
              = Lambda*sqrt(Lambda^2+M^2)/2 - M^2/2 * ln((Lambda+sqrt(Lambda^2+M^2))/M)
    """
    if M <= 0:
        return -m_current

    EL = np.sqrt(Lambda_cut**2 + M**2)
    I_1 = Lambda_cut * EL / 2 - M**2 / 2 * np.log((Lambda_cut + EL) / M)

    rhs = m_current + 2 * G_val * N_colors / pi**2 * M * I_1
    return M - rhs

def solve_gap(m_current, G_val=None, Lambda_cut=None):
    """Solve the NJL gap equation for constituent mass M."""
    if G_val is None:
        G_val = G
    if Lambda_cut is None:
        Lambda_cut = Lambda_NJL

    # Look for non-trivial solution
    try:
        M = brentq(njl_gap_equation, 1.0, Lambda_cut * 2,
                    args=(m_current, G_val, Lambda_cut))
    except ValueError:
        M = m_current  # no DCSB, constituent = current

    return M

# ============================================================================
# QUARK CONDENSATE FROM GAP EQUATION
# ============================================================================

def condensate_from_M(M, Lambda_cut, N_colors=3):
    """Quark condensate from constituent mass (3D cutoff).
    <psi-bar psi> = -N_c/pi^2 * M * I_1(M, Lambda)
    """
    if M <= 0:
        return 0
    EL = np.sqrt(Lambda_cut**2 + M**2)
    I_1 = Lambda_cut * EL / 2 - M**2 / 2 * np.log((Lambda_cut + EL) / M)
    return -N_colors / pi**2 * M * I_1

def fpi_from_gap(M, Lambda_cut, N_colors=3):
    """Pion decay constant from Pagels-Stokar (3D cutoff).
    f_pi^2 = N_c * M^2 / (4*pi^2) * [I_1/E_Lambda - I_2]
    where I_2 = int_0^Lambda dp p^2/(p^2+M^2)^(3/2) = Lambda/E_Lambda - artanh(Lambda/E_Lambda)/Lambda? ...
    Simpler: f_pi^2 = N_c/(4*pi^2) * M^2 * ln(1 + Lambda^2/M^2)  [4D approx]
    """
    if M <= 0:
        return 0
    L2 = Lambda_cut**2
    M2 = M**2
    fpi2 = N_colors / (4 * pi**2) * M2 * np.log(1 + L2 / M2)
    return np.sqrt(max(fpi2, 0))

